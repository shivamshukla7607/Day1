package com.bridge.labz;

public class Primitive_Data_Print {
    public static void main(String[] args) {
        int i=10;
        boolean b=true;
        char ch='a';
        long l=5000;
        float ft=10f;
        double d=10.5;

        System.out.println ("Print int : "+i);
        System.out.println ("Print boolean : "+b);
        System.out.println ("Print char : "+ch);
        System.out.println ("Print long : "+l);
        System.out.println ("Print float : "+ft);
        System.out.println ("Print double : "+d);

    }
}
